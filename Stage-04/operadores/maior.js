/*** Operadores de comparação maior e menor (igual) */

let one = 37
let two = 38

//     >     Maior que
console.log(one > two);

//   >=     Maior igual a
console.log(one >= 37);
console.log(two >= 37);

//     <  Menor que     
console.log(one < two);

//     <=   Menor igual a   
console.log( one <=   two );
console.log( one <=   37 );
console.log( one <=   0 );